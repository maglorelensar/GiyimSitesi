
package entity;


public class Renkler {
    private int r_id;
    private String r_adi;

    public Renkler() {
    }

    public Renkler(int r_id, String r_adi) {
        this.r_id = r_id;
        this.r_adi = r_adi;
    }

    
    public int getR_id() {
        return r_id;
    }

    public void setR_id(int r_id) {
        this.r_id = r_id;
    }

    public String getR_adi() {
        return r_adi;
    }

    public void setR_adi(String r_adi) {
        this.r_adi = r_adi;
    }

  
 
    
}
